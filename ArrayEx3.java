// 1.Write a java program to print array elements.
// I/p int[] a={1,2,3,4,5};
// O/P  1 2 3 4 5  

class ArrayEx3 {
    public static void main(String args[]) {
        int[] a = { 10, 20, 30, 40, 50 };
        for (int i = 0; i < a.length; i++) {
            System.out.println(i);
        }
    }
}
