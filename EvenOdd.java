// 1.Write a java program to check number is even or odd?
class EvenOdd{
    public static void main(String[] args) {
        
        System.out.println("First Code");
        
        int a = 10;
        if (a % 2 == 0) {
            System.out.println(a + " is an Even Number.");
        } else {
            System.out.println(a + " is an Odd Number.");
        }
        
        System.out.println("===================");
    }
}
